    //validation
    public boolean validatation() {
        String email = txt_email.getText();

        if (email.equals("")) {
            JOptionPane.showMessageDialog(this, "Please enter Email!");
            return false;
        } else if (!email.matches("^.+@.+\\..+$")) {
            JOptionPane.showMessageDialog(this, "Please enter valid Email !");
            return false;
        }

        return true;

    }


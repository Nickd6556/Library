    public void SearchEmail() {
        String email = txt_email.getText();

        try {
            Connection con = ConnectionProvider.getCon();
            String sql = "Select * from users where email=?";
            PreparedStatement ps = con.prepareStatement(sql);
            ps.setString(1, email);

            ps.executeQuery();

            ResultSet rs = ps.executeQuery();

            if (rs.next()) {
                ImageIcon icon = new ImageIcon("G:\\My Drive\\Project Editable\\Library_Management_System\\src\\icons\\check.png");
                JOptionPane.showMessageDialog(null, "<html><b style=\"color:green;font-size:13px\">&nbsp;Email Matched !</html>", "Warning", JOptionPane.INFORMATION_MESSAGE, icon);

                setVisible(false);
                new UpdatePassword().setVisible(true);

            } else {

                ImageIcon icon = new ImageIcon("G:\\My Drive\\Project Editable\\Library_Management_System\\src\\icons\\incorrect.png");
                JOptionPane.showMessageDialog(null, "<html><b style=\"color:red;font-size:13px\">&nbsp;Incorrect<br>&nbsp;Email Doesn`t Match !</html>", "Warning", JOptionPane.INFORMATION_MESSAGE, icon);
            }

            //System.out.println(generatedPassword);
        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

